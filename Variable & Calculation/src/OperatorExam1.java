
public class OperatorExam1 {

	public static void main(String[] args) {
		int i = 10;
		int j = 10;
// 단순대입 연산자		
		
		System.out.println(i == j); // 같은지 확인
		System.out.println(i != j); // 다른지 확인
		System.out.println(i < j); // j가 i보다 더 큰지 확인
		System.out.println(i <= j); // j가 i보다 크거나 같은지 확인
		System.out.println(i > j); // i가 j보다 큰지 확인
		System.out.println(i >= j); // i가 j보다 크거나 같은지 확인
// 비교연산자
		
		i += 10; // i = i + 10;
		
		System.out.println(i);
		
		System.out.println(i-=5);
		System.out.println(i);
		
//복합대입 연산자
		
		

	}

}
