
public class OperatorExam2 {

	public static void main(String[] args) {
		int a = 5;
		int b = 10;
		int c = 15;
		
		System.out.println((a - b) * c);
		
		System.out.println(a > 5 && b > 5);
		
		System.out.println(a > 5 || b > 5);
		
		System.out.println(a++ - 5);
		
		System.out.println(a);


// 연산자 우선순위
// && = and 연산자 (논리연산자) 
// || = or 연산자
// 비교연산자가 논리연산자보다 우선순위
		
		

	}

}
